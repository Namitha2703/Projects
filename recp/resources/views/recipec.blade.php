<div class="w3-content" style="max-width:1100px">
  <h1 class="left">PREPARATION</h1><br>
    Step 1<br>
Take a large bowl, add 3 tbsp corn flour and Â½ tsp finely chopped garlic-ginger and mix them. Sprinkle salt and red pepper according to your taste. Mix the ingredients well. Now add paneer cubes in the marinade. Toss the ingredients well and make sure to coat paneer nicely with marinade.<br>

Step 2<br>
Now pour oil in a non-stick pan and heat the oil for a minute. When the oil is heated, deep fry the coated paneer cubes. Once they turn golden brown, take them out and drain the extra oil with a napkin and keep them aside.<br>

Step 3<br>
Now heat the oil again and add chopped onions. Stir fry onions on medium flame, until they become tender and golden brown in color. Once done, add chopped garlic, ginger, green chilies and celery. Mix them.<br>

Step 4<br>
Add capsicum and stir fry the ingredients for another 2-3 minutes. Once done, add soy and tomato sauce in the pan along with water. Stir and simmer for another minute. Meanwhile prepare a thick corn starch mixture by adding water in it. Add the corn starch mixture to thicken the sauce.<br>

Step 5<br>
Now add fried paneer cubes in the pan. Add white vinegar and mix it well in the pan. Season it with salt and black pepper. Stir well and simmer it for a minute.

Step 6<br>
Stir occasionally. When your Manchurian thickens, turn off the flame and garnish it with green spring onions and serve with noodles or fried rice.<br>  
