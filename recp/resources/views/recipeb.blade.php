<h1 class="left">INGREDIANTS</h1><br>
Ingredients for Dry Chilli Chicken Recipe<br>
Boneless chicken cut into cubes 400 grams<br>
Red chilli powder 1 teaspoon<br>
Green chillies halved and slit 2-3<br>
Salt to taste<br>
Crushed black peppercorns to taste<br>
Ginger-garlic paste 1 tablespoon<br>
Oil 1 tablespoon + to deep fry<br>
Refined flour (maida) ¼ cup<br>
Cornflour/ corn starch ¼ cup + 1 tablespoon<br>
Garlic chopped 1 tablespoon<br>
Onion cut into cubes 1 medium<br>
Green capsicum diced 1½ medium<br>
Tomato ketchup 1 tablespoon<br>
Soy sauce 2 tablespoons<br>
Ginger cut into thin strips 1 inch<br>
Spring onion greens chopped 1-2 stalks<br>
Cooked vegetable noodles for serving<br>


</div>
<div class="w3-content" style="max-width:1100px">
  <h1 class="left">PREPARATION</h1><br>
Step 1<br>
Take chicken cubes in a bowl. Add salt, crushed peppercorns and ginger-garlic paste and mix well.<br>
Step 2<br>
Heat sufficient oil in a pan.<br>
Step 3<br>
Take refined flour in another bowl. Add ¼ cup cornflour and some water and whisk well to make a semi-thick slurry.<br>
Step 4<br>
Dip marinated chicken cubes in the slurry, slide into hot oil and deep-fry till golden and crisp. Drain on absorbent paper.<br>
Step 5<br>
Heat 1 tablespoon oil in a non-stick pan. Add garlic and sauté till golden. Add onion and sauté on high heat for 30 seconds.<br>
Step 6<br>
Add capsicum and mix. Add fried chicken cubes and mix. Add tomato ketchup, soy sauce, salt, crushed peppercorns and chilli powder and mix well.<br>
Step 7<br>
Add green chillies and ginger strips and mix.<br>
Step 8<br>
Take remaining cornflour in another bowl. Add some water and mix well to make a smooth slurry.<br>
Step 9<br>
Add slurry to chicken mixture, mix and cook till the mixture thickens. Add spring onion greens, switch off heat and mix.<br>
Step 10<br>
Put some noodles on a serving platter. Put the dry chilli chicken on top and serve hot.<br>
      
