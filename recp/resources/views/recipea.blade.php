<h1 class="left">INGREDIANTS</h1><br>

For Florets<br>
1/2 cup Corn Flour<br>
5 tablespoons Maida (all purpose flour)<br>
1 medium size Cauliflower (Phool Gobi), broken into medium size florets<br>
1/2 teaspoon Garlic Paste<br>
1/2 teaspoon Ginger Paste<br>
Oil, for deep frying<br>
Salt<br>
1/2 cup Water<br>
For Sauté<br>
1½ teaspoons Garlic Paste<br>
2 teaspoons Ginger Paste<br>
2 Green Chillies, finely chopped<br>
1 large Onion, finely chopped<br>
1 small Green Capsicum, finely chopped<br>
1½ tablespoons Soy Sauce<br>
1/2 tablespoon Chilli Sauce<br>
2 tablespoons Tomato Ketchup/ Tomato Sauce<br>
2 tablespoons Cooking Oil<br>
2 tablespoons Spring Onion, finely chopped<br>
Salt<br>
</div>
<div class="w3-content" style="max-width:1100px">
  <h1 class="left">PREPARATION</h1><br>
      
      <p class="w3-large">
Step-1<br>
Boil cauliflower florets in salted water over medium flame for 3-4 minutes. After boiling, drain excess water and pat dry florets on tissue paper.<br>
Step-2<br>
Mix maida, corn flour, 1/2 teaspoon ginger paste, 1/2 teaspoon garlic paste and salt in a bowl. Add water (approx. 1/2 cup) little by little and stir to make a batter. Make sure that batter is not too thick or not too thin. Its thickness should be similar to thickness of dosa batter. Add all florets in batter and mix well. For perfect manchurian, batter should coat florets evenly.<br>
Step-3<br>
Heat oil in kadai (frying pan) over medium flame. Carefully drop or slide 6 to 8 florets in oil and deep fry over medium flame until they turn golden brown. Drain and transfer deep fried florets over paper napkin to a plate. Deep fry remaining florets.<br>
Directions for Sauté:<br>
Step-4<br>
Heat 2-tablespoons oil in a wide, thin-bottomed pan or wok on medium flame. Add 2 teaspoons ginger paste, 1½ teaspoons garlic paste, chopped green chillies, chopped capsicum and chopped onion.<br>
Step-5<br>
Sauté them on high flame for 3-4 minutes.<br>
Step-6<br>
Add soy sauce, tomato ketchup, chilli sauce and salt.<br>
Step-7<br>
Stir continuous and cook for a minute<br>
Step-8<br>
Add deep fried florets.<br>
Step-9<br>
Toss and mix deep fried florets with other ingredients in pan for 1-2 minutes keeping flame on high. (Tossing everything on high heat in wok or pan is must for making best Chinese gobi manchurian dry.)<br>
Step-10<br>
Transfer it to a plate and garnish with spring onion. Serve it hot with tomato ketchup and chilli sauce.<br>

