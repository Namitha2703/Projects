<h1 class="left">INGREDIANTS</h1><br>
3 tablespoons butter<br>
250 grams Paneer cubes (or Cottage Cheese)<br>
3 Kashmiri Red Chilies<br>
1 teaspoon Ginger Paste<br>
1 teaspoon Garlic Paste<br>
1 Bay leaves<br>
1 inch Cinnamon Stick<br>
3 Cloves<br>
2 Cardamoms<br>
1/2 teaspoon Peppercorns<br>
2 tablespoons Cashewnuts (Chopped)<br>
1 teaspoon White Poppy Seeds (Khus Khus (Optional))<br>
1 Onion, roughly chopped<br>
6 Tomatoes, roughly chopped<br>
1/2 teaspoon Chili powder<br>
1/2 teaspoon Garam masala (Powder)<br>
1/2 teaspoon Turmeric (Powder)<br>
1 1/2 tablespoons Ketchup<br>
1 tablespoons Kasuri Methi (Dry)<br>
2 tablespoons Fresh Cream<br>
to taste Salt<br>


</div>
<div class="w3-content" style="max-width:1100px">
  <h1 class="left">PREPARATION</h1><br>
  Step-1<br>
  In a large pan or kadhai, heat a tablespoon of butter and a tablespoon of oil.<br>
  Step-2<br>
Add red chillies, ginger, garlic paste and all the whole spices (bay leaves, cinnamon, cloves, cardamom and peppercorns). Saute for a minute or two and add cashew nuts, poppy seeds (if using) and onions. Once the onions turn translucent, add the tomatoes. Mix well.<br>
Step-3<br>
Cover and cook till the tomatoes start breaking down and the mixture starts leaving oil at the edges. Switch off the flame and set aside to cool.<br>
Step-4<br>
Once cool enough to handle, fish out as many whole spices as possible, and blend the mixture to a smooth paste.<br>
Step-5<br>
In the same pan or kadhai, heat the remaining butter and add the blended paste prepared in step #4. Add garam masala powder, chili powder, turmeric, ketchup, salt along with 1/2 cup water. Bring this to a boil.<br>
Step-6<br>
Once the curry comes to a boil, simmer and cook for 30 minutes, till the oil separates slightly. Cut the paneer into bite sized pieces, add it to the gravy and mix gently.<br>
Step-7<br>
In another pan, dry roast the kasuri methi and grind it to a fine powder. Mix kasuri methi and fresh cream in the curry and switch off the flame.<br>

