<h1 class="left">INGREDIANTS</h1><br>
2 large Ripe Mangoes(approx. 2 cups chopped mango)<br>
1½ cups Milk<br>
1½ tablespoons Sugar(or to taste)<br>
2-3 Ice Cubes, optional<br>
</div>
<div class="w3-content" style="max-width:1100px">
  <h1 class="left">PREPARATION</h1><br>
  Step-1<br>
  Wash, peel and chop mangoes into small pieces, discard stone. Transfer them to a blender jar.<br>
  Step-2<br>
  Add milk, sugar (to taste or according to sweetness of mango) and ice-cubes.<br>
  Step-3<br>
  Blend until smooth and creamy and there are no mango chunks in it. Pour prepared shake into serving glasses, garnish with almonds and scoop of ice cream and serve.<br>



